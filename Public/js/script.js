function updateCarouselCaption() {
  const carouselItems = document.querySelectorAll('.carousel-item');
  const textDiv = document.getElementById('carouselText'); // Make sure this element exists
  const activeSlide = document.querySelector('.carousel-item.active');
  const activeIndex = [...carouselItems].indexOf(activeSlide);

  if (textDiv && activeIndex !== -1) {
    textDiv.textContent = translations[currentLang].carousel[activeIndex];


  }
}

// Carousel slide event to update caption on slide change
document.addEventListener('DOMContentLoaded', () => {
  const carousel = document.getElementById('imageCarousel');
  const textDiv = document.getElementById('carouselText');

  if (!carousel || !textDiv) return;

  carousel.addEventListener('slid.bs.carousel', function (event) {
    const carouselItems = document.querySelectorAll('.carousel-item');
    const nextSlide = event.relatedTarget;
    const nextIndex = [...carouselItems].indexOf(nextSlide);

    if (nextIndex !== -1) {
      // Update text content
      textDiv.textContent = translations[currentLang].carousel[nextIndex];

    }
  });

  // Initialize language based on stored preference or default
  setLanguage(currentLang);
});
